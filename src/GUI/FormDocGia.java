    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            Integer maSach = (Integer) jTable1.getValueAt(selectedRow, 0);
            String tenSach = (String) jTable1.getValueAt(selectedRow, 1);
            String tacGia = (String) jTable1.getValueAt(selectedRow, 2);
            String nhaSanXuat = (String) jTable1.getValueAt(selectedRow, 3);
            String namXuatBan = (String) jTable1.getValueAt(selectedRow, 4);
            String cmnd = (String) jTable1.getValueAt(selectedRow, 5);

            // Gán giá trị vào các ô text
            txt_maDocGia.setText(maSach.toString());
            txt_tenDocGia.setText(tenSach);
            txt_ngaySinh.setText(tacGia);
            txt_chucDanh.setText(nhaSanXuat);
            txt_diaChi.setText(namXuatBan);
            txt_cmnd.setText(cmnd);
           
        }
    }//GEN-LAST:event_jTable1MouseClicked

