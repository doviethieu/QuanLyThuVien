    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                Date ngaySinh = null;
                try {
                    ngaySinh = dateFormat.parse(txt_ngaySinh.getText());
                } catch (ParseException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(rootPane, "Ngày sinh không hợp lệ! Hãy nhập kiểu dd/MM/yyyy");
                    return;
                }
                DocGia newobj = new DocGia(0, txt_tenDocGia.getText(),
                        ngaySinh, txt_chucDanh.getText(), txt_diaChi.getText(), txt_cmnd.getText());
        if(DocGiaDAO.addDocGia(newobj)){
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            LoadData();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                Date ngaySinh = null;
                try {
                    ngaySinh = dateFormat.parse(txt_ngaySinh.getText());
                } catch (ParseException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(rootPane, "Ngày sinh không hợp lệ! Hãy nhập kiểu dd/MM/yyyy");
                    return;
                }
                DocGia newobj = new DocGia(Integer.parseInt(txt_maDocGia.getText()), txt_tenDocGia.getText(),
                        ngaySinh, txt_chucDanh.getText(), txt_diaChi.getText(), txt_cmnd.getText());
        if(DocGiaDAO.updateDocGia(newobj)){
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
            LoadData();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Sửa thất bại");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

