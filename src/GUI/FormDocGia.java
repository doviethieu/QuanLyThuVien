    private void LoadData() {
    try {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã độc giả");
        model.addColumn("Tên độc giả");
        model.addColumn("Ngày sinh");
        model.addColumn("Chức danh");
        model.addColumn("Địa chỉ");
        model.addColumn("Số CMND");
        List<DocGia> docGias = DocGiaDAO.getAllDocGia();

        // Thêm dữ liệu từ danh sách Độc giả vào model
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        for (DocGia docGia : docGias) {
            Object[] row = {
                    docGia.getMaDocGia(),
                    docGia.getHoTen(),
                    dateFormat.format(docGia.getNgaySinh()), // Định dạng lại ngày sinh
                    docGia.getChucDanh(),
                    docGia.getDiaChi(),
                    docGia.getSoCMND()
            };
            model.addRow(row);
        }

        // Set model cho jTable1
        jTable1.setModel(model);
    } catch (Exception ex) {
        ex.printStackTrace();
        // Xử lý lỗi nếu cần
    }
    txt_maDocGia.setText("");
    txt_tenDocGia.setText("");
    txt_ngaySinh.setText("");
    txt_chucDanh.setText("");
    txt_diaChi.setText("");
    txt_cmnd.setText("");
    
}
