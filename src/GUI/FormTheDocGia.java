    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String soThe = (String) jTable1.getValueAt(selectedRow, 0);
        
            String tacGia = (String) jTable1.getValueAt(selectedRow, 2);
            String nhaSanXuat = (String) jTable1.getValueAt(selectedRow, 3);
            Integer namXuatBan = (Integer) jTable1.getValueAt(selectedRow, 4);

            // Gán giá trị vào các ô text
            txt_soThe.setText(soThe);
         
            txt_ngayCap.setText(tacGia);
            txt_ngayHetHan.setText(nhaSanXuat);
            txt_gioiHanMuon.setText(namXuatBan.toString());

        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date ngayCap = null;
        Date ngayHetHan = null;
        try {
            ngayCap = dateFormat.parse(txt_ngayCap.getText());
            ngayHetHan = dateFormat.parse(txt_ngayHetHan.getText());
        } catch (ParseException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Ngày không hợp lệ! Hãy nhập kiểu dd/MM/yyyy");
            return;
        }
        TheDocGia newobj = new TheDocGia(txt_soThe.getText(), Integer.parseInt(cbx_maDocGia.getSelectedItem().toString()), ngayCap, ngayHetHan,
                Integer.parseInt(txt_gioiHanMuon.getText()));
        if(TheDocGiaDAO.addTheDocGia(newobj)){
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            LoadData();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

