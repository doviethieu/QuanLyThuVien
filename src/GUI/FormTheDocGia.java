    private void LoadData() {
    try {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Số thẻ");
        model.addColumn("Mã độc giả");
        model.addColumn("Ngày cấp");
        model.addColumn("Ngày hết hạn");
        model.addColumn("Giới hạn mượn");
        List<TheDocGia> theDocGias = TheDocGiaDAO.getAllTheDocGia();

        // Thêm dữ liệu từ danh sách Độc giả vào model
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        for (TheDocGia theDocGia : theDocGias) {
            Object[] row = {
                    theDocGia.getSoThe(),
                    theDocGia.getMaDocGia(),
                    dateFormat.format(theDocGia.getNgayCap()), // Định dạng lại ngày sinh
                    dateFormat.format(theDocGia.getNgayHetHan()),
                    theDocGia.getGioiHanMuon()
            };
            model.addRow(row);
        }

        // Set model cho jTable1
        jTable1.setModel(model);
    } catch (Exception ex) {
        ex.printStackTrace();
        // Xử lý lỗi nếu cần
    }
    txt_soThe.setText("");
   
    txt_ngayCap.setText("");
    txt_ngayHetHan.setText("");
    txt_gioiHanMuon.setText("");
    List<DocGia> docGia = DocGiaDAO.getAllDocGia();
        DefaultComboBoxModel<DocGia> spModel = new DefaultComboBoxModel<>(docGia.toArray(new DocGia[0]));
        cbx_maDocGia.setModel(spModel);
    }
